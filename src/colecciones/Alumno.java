package colecciones;

/**
 *
 * @author joaqu
 */
class Alumno 
{
    
    public String Nombre;
    public int Edad;

    public Alumno() {
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

    public int getEdad() {
        return Edad;
    }

    public void setEdad(int Edad) {
        this.Edad = Edad;
    }

    @Override
    public String toString() {
        return "Alumno{" + "Nombre=" + Nombre + ", Edad=" + Edad + '}';
    }

  
    
    


}
